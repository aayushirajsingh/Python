# Program a calculator using Python

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2
mod = num1 % num2

print(f"Addition= {add}")
print(f"Substraction= {sub}")
print(f"Multiplication= {mul}")
print(f"Division= {div}")
print(f"Modulus= {mod}")

